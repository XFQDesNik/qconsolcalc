package ru.digitos;

public class Operations {
    int firstNumber = 0;
    int secondNumber = 0;

    int addition() {
        return firstNumber + secondNumber;
    }

    int difference() {
        return firstNumber - secondNumber;
    }

    int division() {
        return firstNumber / secondNumber;
    }

    int multiplication() {
        return firstNumber * secondNumber;
    }
}
